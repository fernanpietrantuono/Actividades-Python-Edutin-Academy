# El programa pide los números
num1 = int(input("Ingresá un primer número: "))
num2 = int(input("Ingresá un segundo número: "))

# Aritméticos
print(f'La suma de ambos números es {num1+num2}')
print(f'La multiplicación de ambos números es {num1*num2}')

# Relacionales
print(f'¿{num1} y {num2} son iguales? {num1 == num2}')
print(f'¿{num1} es menor que {num2}? {num1 < num2}')
print(f'¿{num2} es mayor o igual que {num1}? {num2 >= num1}')
